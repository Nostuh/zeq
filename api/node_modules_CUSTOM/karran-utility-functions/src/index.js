uf.await_dloop = async function(data,call) {
    try {
        //Try object array thing!
        if ( typeof data == "object" ) {
            for( let [i,v] of Object.entries(data) ) {
                await call(i,v);
            }
            return true;
        }    

        //Try normal array!
        else if ( Array.isArray(data) ) {
            for(let [i,v] of data) {
                await call(i,v);
            }
            return true;
        }
    } catch (e) {
        console.log(e);
        console.log(data);
        throw e;
    }

    return false;
}


