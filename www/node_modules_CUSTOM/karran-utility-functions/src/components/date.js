import moment from 'moment';
import { puf } from '../index.js';


export const DateService = {

    get_datetime: function(milli=false) {
        let date_ob = new Date();
        
        // adjust 0 before single digit date
        let date = ("0" + date_ob.getDate()).slice(-2);
        // current month
        let month = ("0" + (date_ob.getMonth() + 1)).slice(-2);
        // current year
        let year = date_ob.getFullYear();
        // current hours
        let hours = date_ob.getHours();
        // current minutes
        let minutes = date_ob.getMinutes();
        // current seconds
        let seconds = date_ob.getSeconds();
    
        if ( milli ) {
            return year + "-" + month + "-" + date + " " + puf.left_pad(hours,2,"0") + ":" + puf.left_pad(minutes,2,"0") + ":" + puf.left_pad(seconds,2,"0") + ":" + puf.left_pad(mili,3,"0") + " ";
        } else {
            return year + "-" + month + "-" + date + " " + puf.left_pad(hours,2,"0") + ":" + puf.left_pad(minutes,2,"0") + ":" + puf.left_pad(seconds,2,"0") + " ";    
        }    
    },

    offset_lll: function(value) {
        try {
            if ( this.is_invalid_string(value) ) {
                return "";
            } else {
                return moment(String(value)).utcOffset(0).format('lll');
            }
        } catch (e) {
            console.log(e);
            throw e;
        }

    },

    mssql: function(value) {
        try {
            if ( this.is_invalid_string(value) ) {
                return "";
            } else {
                return value.substring(0,10)+" "+value.substring(11,23);
            }
        } catch (e) {
            console.log(e);
            throw e;
        }
    },

    is_invalid_string: function(v) {
        return v == undefined || v == null || (typeof v == "string" && v.trim() == "");
    }
}